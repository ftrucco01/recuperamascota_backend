     * @param  \App\Models\User  $user
     * @param  \App\Models\User  $model
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function update(User $user, User $model)
    {
        $response = false;

        if ($user->hasRole('administrator') && $model->hasRole('superadministrator')) {
            $response = false;
        }

        if ($user->hasAnyRole('administrator', 'superadministrator')) {
            $response = true;
        }

        if ($user->id == $model->registry_id) {
            $response = true;
        }

        return $response;
    }

